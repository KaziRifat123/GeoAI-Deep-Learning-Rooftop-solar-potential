@tf.keras.utils.register_keras_serializable()
def dice_focal_loss(y_true, y_pred, gamma=2.0, alpha=0.25):
    y_true = tf.cast(y_true, tf.float32)
    y_pred = tf.clip_by_value(tf.cast(y_pred, tf.float32), 1e-7, 1 - 1e-7)

    # Dice part
    intersection = tf.reduce_sum(y_true * y_pred)
    dice = 1 - (2. * intersection + 1e-6) / (tf.reduce_sum(y_true) + tf.reduce_sum(y_pred) + 1e-6)

    # Focal part
    bce = -y_true * tf.math.log(y_pred) * alpha * tf.math.pow(1 - y_pred, gamma) \
          - (1 - y_true) * tf.math.log(1 - y_pred) * (1 - alpha) * tf.math.pow(y_pred, gamma)
    focal = tf.reduce_mean(bce)

    return dice + focal


