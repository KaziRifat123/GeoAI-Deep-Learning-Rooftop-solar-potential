import tensorflow as tf

def augment(image, mask):
    image = tf.image.convert_image_dtype(image, tf.float32)
    mask = tf.cast(mask, tf.float32)

    # Flips
    if tf.random.uniform(()) > 0.5:
        image = tf.image.flip_left_right(image)
        mask = tf.image.flip_left_right(mask)
    if tf.random.uniform(()) > 0.5:
        image = tf.image.flip_up_down(image)
        mask = tf.image.flip_up_down(mask)

    # Rotation
    if tf.random.uniform(()) > 0.5:
        k = tf.random.uniform((), 0, 4, dtype=tf.int32)
        image = tf.image.rot90(image, k)
        mask = tf.image.rot90(mask, k)

    # Brightness & contrast (mild)
    if tf.random.uniform(()) > 0.5:
        image = tf.image.random_brightness(image, 0.1)
    if tf.random.uniform(()) > 0.5:
        image = tf.image.random_contrast(image, 0.95, 1.05)

    # Light Gaussian noise
    if tf.random.uniform(()) > 0.5:
        noise = tf.random.normal(tf.shape(image), stddev=0.01)
        image = tf.clip_by_value(image + noise, 0.0, 1.0)

    # Zoom-in (safe range)
    if tf.random.uniform(()) > 0.5:
        scale = tf.random.uniform([], 0.85, 1.0)
        crop_size = tf.cast(scale * 256, tf.int32)
        offset = (256 - crop_size) // 2
        image = tf.image.crop_to_bounding_box(image, offset, offset, crop_size, crop_size)
        mask = tf.image.crop_to_bounding_box(mask, offset, offset, crop_size, crop_size)
        image = tf.image.resize(image, [256, 256])
        mask = tf.image.resize(mask, [256, 256], method='nearest')

    # Ensure binary mask
    mask = tf.where(mask > 0.5, 1.0, 0.0)
    return image, mask

# === Use your existing augment() function ===
def augment_numpy(img, mask):
    # Convert NumPy to Tensor
    img = tf.convert_to_tensor(img, dtype=tf.float32)
    mask = tf.convert_to_tensor(mask, dtype=tf.float32)

    # Apply augmentation
    img, mask = augment(img, mask)

    # Convert back to NumPy
    img = img.numpy()
    mask = mask.numpy()
    return img, mask

# === Custom Data Generator ===
def data_generator(image_list, mask_list, batch_size, do_augment=False):
    while True:
        indices = list(range(len(image_list)))
        random.shuffle(indices)
        for i in range(0, len(indices), batch_size):
            batch_images, batch_masks = [], []
            batch_ids = indices[i:i + batch_size]
            for idx in batch_ids:
                img = cv2.imread(image_list[idx])
                img = cv2.resize(img, (256, 256))
                img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB).astype(np.float32) / 255.0

                mask = cv2.imread(mask_list[idx], cv2.IMREAD_GRAYSCALE)
                mask = cv2.resize(mask, (256, 256), interpolation=cv2.INTER_NEAREST)
                mask = (mask > 127).astype(np.float32)
                mask = np.expand_dims(mask, axis=-1)

                if do_augment:
                    img, mask = augment_numpy(img, mask)

                batch_images.append(img)
                batch_masks.append(mask)

            yield np.stack(batch_images), np.stack(batch_masks)

