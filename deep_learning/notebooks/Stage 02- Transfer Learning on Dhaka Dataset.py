import os
import numpy as np
import pandas as pd
import shutil
import matplotlib.pyplot as plt
from PIL import Image
import tensorflow as tf
from tensorflow.keras.preprocessing.image import load_img, img_to_array

# === Constants ===
IMAGE_FOLDER = '/content/drive/MyDrive/DL/new anno/images'
MODEL_PATH = '/content/drive/MyDrive/DL/my_model_v6.h5'
OUTPUT_FOLDER = 'overlay_results_yellow_only'
ZIP_FILENAME = 'overlay_results_yellow_only.zip'
CSV_FILENAME = 'solar_generation_estimates.csv'

SOLAR_IRRADIANCE = 4.5     # kWh/m²/day
EFFICIENCY = 0.18          # 18%
PIXEL_AREA_M2 = 0.0625  # m² per pixel (based on Google Earth scale)

IMAGE_SIZE = (256, 256)

# === Load model ===
model = tf.keras.models.load_model(MODEL_PATH, compile=False)

# === Prepare output directory ===
if os.path.exists(OUTPUT_FOLDER):
    shutil.rmtree(OUTPUT_FOLDER)
os.makedirs(OUTPUT_FOLDER)

# === Process images ===
results = []

for filename in sorted(os.listdir(IMAGE_FOLDER)):
    if not filename.lower().endswith(('.png', '.jpg', '.jpeg')):
        continue

    img_path = os.path.join(IMAGE_FOLDER, filename)

    # Load and preprocess image
    img = load_img(img_path, target_size=IMAGE_SIZE)
    img_array = img_to_array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    # Predict mask
    pred = model.predict(img_array)[0].squeeze()
    binary_mask = (pred > 0.5).astype(np.uint8)

    # Compute area and energy
    usable_pixels = np.sum(binary_mask)
    usable_area_m2 = usable_pixels * PIXEL_AREA_M2
    daily_kwh = usable_area_m2 * SOLAR_IRRADIANCE * EFFICIENCY
    monthly_kwh = daily_kwh * 30
    yearly_kwh = daily_kwh * 365

    # Overlay yellow rooftop area
    original = np.array(img.resize(IMAGE_SIZE)).astype(np.uint8)
    overlay = original.copy()
    overlay[binary_mask == 1] = (0.6 * original[binary_mask == 1] + 0.4 * np.array([255, 255, 0])).astype(np.uint8)

    # Plot and annotate
    fig, ax = plt.subplots(figsize=(6, 6))
    ax.imshow(overlay)
    ax.set_title(f"{filename}", fontsize=10)
    ax.axis("off")

    text = f"Usable Area: {usable_area_m2:.2f} m²\nEst. Daily Gen: {daily_kwh:.2f} kWh"
    ax.text(5, 20, text, fontsize=10, color='black', backgroundcolor='white', alpha=0.7)

    # Save annotated image
    save_path = os.path.join(OUTPUT_FOLDER, filename)
    plt.tight_layout()
    plt.savefig(save_path, bbox_inches='tight')
    plt.close()

    # Save data
    results.append({
        "Image": filename,
        "Usable Area (m²)": round(usable_area_m2, 2),
        "Daily Generation (kWh)": round(daily_kwh, 2),
        "Monthly Generation (kWh)": round(monthly_kwh, 2),
        "Yearly Generation (kWh)": round(yearly_kwh, 2)
    })

# === Save CSV separately ===
csv_path = os.path.join(OUTPUT_FOLDER, CSV_FILENAME)
df = pd.DataFrame(results)
